		<p align="left"><strong>Why Sponsor BSidesIndy?</strong><br />
		  We are a group of people who believe Information Security can be improved by strengthening the community. We do rely on the generosity of corporate
		  sponsors, and at times wind up funding this event out of our own pockets. In return the community benefits in the form of increased interconnectedness, 
		  bringing students, job seekers, employers, and seasoned industry pros together under one roof. 
		</p>
		<p>Since our first event on February 22, 2015, we've worked to add value to the industry and our community by providing free activities including 
		speakers, resume workshops, IoT hacking village, lockpicking village, <a href="https://twitter.com/Hak4Kidz" target="_top">kid focused activities</a>, and 
		more. In 2018 we hope to formalize the workshops we started last year into full trainings. The goal is to engage and educate present and future members of
		the Information Security profession. We're noticing an uptick in attendees from local universities including Indiana University, Purdue, and IUPUI. This 
		event drew about 200 local/regional participants last year, including staff and speakers from around the United States. 
